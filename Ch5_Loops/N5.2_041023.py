# --- --- --- Definite Loops --- --- ---
# Will run for finite amount of times. 
# They will only run for not a long time.

# -- -- -- (SDL) Simple Definite Loop Example -- -- --
for i in [5, 4, 3, 2 , 1] : # i will be assigned to the numbers inside the bracket.
    print(i)                # i will print 5 times as there is 5 numbers in the bracket.

print('Blastoff!')

# -- -- -- (DLS) Definite Loop with Strings -- -- --
friends = ['Joseph', 'Glenn', 'Sally']  # CUSTOM VARIABLE.

for friend in friends :                 # Similar to the example on SDL.
    print('Happy New Year:', friend)
print('Done!')

